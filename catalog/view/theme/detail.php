            <div class="col-md-8 text-justify " style="margin-top: 20px;">
                <h5 class="text-danger text-center font-weight-bold">สถานเอกอัครราชทูตไทย ณ กรุงโซล ประกาศเตือนคนไทยในเกาหลีใต้ ระวังติดเชื้อโควิด-19 หลังมีการแพร่ระบาดอย่างหนักในเกาหลีใต้</h5>
                <p>จากกรณีการแพร่ระบาดของเชื้อไวรัสโคโรนาสายพันธุ์ใหม่ หรือ โควิด-19 ที่มีการแพร่ระบาดอย่างรวดเร็วในประเทศเกาหลีใต้ โดยตรวจพบผู้ติดเชื้อในเกาหลีใต้แล้ว 156 ราย</p>
                <p>วันนี้ (21 ก.พ. 63) สถานเอกอัครราชทูต ณ กรุงโซล ออกประกาศเตือนประชาชนชาวไทยที่อยู่ในประเทศเกาหลีใต้ ให้ระมัดระวังการสัมผัสเชื้อและออกห่างจากพื้นที่บริเวณเสี่ยงติดเชื้อ โดยมีข้อความประกาศดังนี้</p>
                <p>“ตามที่ปรากฏรายงานข่าวเกี่ยวกับการแพร่ระบาดของโรคปอดอักเสบจากเชื้อไวรัสโคโรนาสายพันธุ์ใหม่ (COVID-19) ในเกาหลีใต้อย่างรวดเร็วในช่วงสัปดาห์ที่ผ่านมา โดยล่าสุดมีผู้ติดเชื้อในเกาหลีใต้แล้ว 156 ราย โดยเฉพาะในเมืองแทกูและพื้นที่โดยรอบจังหวัดคยองซังเหนือ พบผู้ติดเชื้อทั้งสิ้น 111 ราย (สถานะ ณ วันที่ 21 กุมภาพันธ์ 2563) นั้น</p>
                <p>
                    สถานเอกอัครราชทูตฯ จึงขอประกาศเตือนชาวไทยในสาธารณรัฐเกาหลีทุกท่าน ให้หลีกเลี่ยงการเดินทางไปยังพื้นที่ที่พบผู้ติดเชื้อจำนวนมาก สำหรับผู้ที่อาศัยอยู่ในพื้นที่ดังกล่าว ขอให้หลีกเลี่ยงการเดินทางไปยังพื้นที่เสี่ยงหรือมีกิจกรรมการชุมนุมของคนจำนวนมาก ระมัดระวังดูแลรักษาสุขภาพให้แข็งแรง หมั่นล้างมือให้สะอาด ใช้ช้อนกลาง สำหรับผู้ที่มีอาการไอ จาม มีไข้ หรือมีความผิดปกติบริเวณทางเดินหายใจ หรือสงสัยว่าบุคคลในครอบครัวของท่านจะติดเชื้อดังกล่าว
                    ให้รีบพบแพทย์เพื่อรับการตรวจวินิจฉัย หรือหากพบกรณีการเจ็บป่วยต้องสงสัย ขอให้ติดต่อกรมควบคุมโรคสาธารณรัฐเกาหลี ที่หมายเลขโทรศัพท์ 1339 หรือสำนักงานตรวจคนเข้าเมืองสาธารณรัฐเกาหลี ซึ่งเปิดบริการให้คำปรึกษาและช่วยเหลือกรณีที่เกี่ยวข้องกับ COVID-19 แก่ชาวต่างชาติที่พำนักอยู่เกาหลีใต้ ตลอด 24 ชั่วโมง โดยสามารถติดต่อไปที่หมายเลขโทรศัพท์ 1345 ต่อ 0*5* สำหรับภาษาไทย (เฉพาะวันจันทร์ - วันศุกร์ เวลา 09.00 – 18.00 น.)
                </p>
